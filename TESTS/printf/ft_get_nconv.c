#include "ft_printf.h"

int		ft_get_nconv(const char *fmt)
{
	int		i;
	int		n;

	i = 0;
	n = 0;
	if (fmt)
		while (fmt[i])
		{
			if (fmt[i] == '%' && fmt[i + 1] != '%')
				n++;
		}
	return (n);
}
