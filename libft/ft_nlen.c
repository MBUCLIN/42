#include "includes/libft.h"

int		ft_nlen(int n)
{
	int		len;

	len = 0;
	if (!n)
		return (1);
	if (n < 0)
	{
		len += 1;
		n *= -1;
	}
	while (n)
	{
		len++;
		n /= 10;
	}
	return (len);
}
