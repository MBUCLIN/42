#include <unistd.h>

void	ft_putnbr(int nbr)
{
	if (nbr * -1 - 1 == 2147483647)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nbr < 0)
	{
		write(1, "-", 1);
		nbr = nbr * -1;
	}
	if (nbr / 10 != 0)
		ft_putnbr(nbr / 10);
	nbr = (nbr % 10) + 48;
	write(1, &nbr, 1);
}
