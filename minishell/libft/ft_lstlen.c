#include "includes/libft.h"

int		ft_lstlen(t_list *head)
{
	int			i;
	t_list		*tmp;

	i = 0;
	tmp = head;
	while (tmp)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
