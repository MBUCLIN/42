int			ft_islm(char *str)
{
	int		i;

	i = 0;
	if ((str[i] == 'l' && str[i + 1] == 'l') ||\
		(str[i] == 'h' && str[i + 1] == 'h'))
		return (1);
	else if (str[i] == 'l' || str[i] == 'h' || str[i] == 'z' || str[i] == 'j')
		return (1);
	return (0);
}
